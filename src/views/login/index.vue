<template>
  <div class="login-container">
    <el-form class="login-form" autoComplete="on" :model="loginForm" :rules="loginRules" ref="loginForm" label-position="left">
      <div class="title-container">
        <h3 class="title">后台管理系统登录</h3>
      </div>
      <el-form-item prop="username">
        <span class="svg-container svg-container_login">
          <svg-icon icon-class="user" />
        </span>
        <el-input name="username" type="text" v-model="loginForm.username" autoComplete="on" placeholder="用户名" />
      </el-form-item>

      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input name="password" :type="passwordType" @keyup.enter.native="handleLogin" v-model="loginForm.password" autoComplete="on" placeholder="密码" />
        <span class="show-pwd" @click="showPwd">
          <svg-icon icon-class="eye" />
        </span>
      </el-form-item>

      <el-form-item prop="smscode">
        <span class="svg-container">
          <svg-icon icon-class="smscode" />
        </span>
        <el-input class="width55" type="text" name="smscode" v-model="loginForm.smscode" placeholder="smscode" clearable="" autoComplete="on"/>
        <span v-if="sendMsgDisabled" class="show-smscode" @click="showSMScode">
            获取短信验证码
        </span>
        <span v-if="!sendMsgDisabled" class="show-smscode">
          {{time}}秒后重新获取
        </span>
      </el-form-item>

      <el-button type="primary" style="width:100%;margin-bottom:30px;" :loading="loading" @click.native.prevent="handleLogin">登录</el-button>

    </el-form>
  </div>
</template>
 
<script type="text/ecmascript-6">
import { isvalidUsername } from '@/utils/validate'

export default {
  name: 'login',
  data() {
    const validateUsername = (rule, value, callback) => {
      if (!isvalidUsername(value)) {
        callback(new Error('请输入正确的用户名'))
      } else {
        callback()
      }
    }
    const validatePassword = (rule, value, callback) => {
      if (value.length < 6) {
        callback(new Error('密码不能少于6位数'))
      } else {
        callback()
      }
    }
    const validateSmscode = (rule, value, callback) => {
      if (!value) {
        callback(new Error('The smscode can not be empty'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: '15112304365',
        password: '123456',
        smscode: '8564'
      },
      loginRules: {
        username: [{ required: true, trigger: 'blur', validator: validateUsername }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }],
        msscode: [{ required: true, trigger: 'blur', validator: validateSmscode }]
      },
      passwordType: 'password',
      loading: false,
      sendMsgDisabled: true,
      time: 60
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
    },
    showSMScode() {
      const that = this
      that.sendMsgDisabled = false
      const interval = window.setInterval(() => {
        if ((that.time--) <= 0) {
          that.time = 60
          that.sendMsgDisabled = true
          window.clearInterval(interval)
        }
      }, 1000)
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('LoginByUsername', this.loginForm).then((res) => {
            this.loading = false
            this.$router.push({ path: '/' })
          }).catch(() => {
            this.loading = false
          })
        } else {
          console.log('提交错误!')
          return false
        }
      })
    }
  },
  created() {
  }
}
</script>

